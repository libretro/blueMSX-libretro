/* MameYM2151. clock=3579545, sampfreq=clock/64=55930                  */
/* ================================================================== */
#include "phaseinc_rom.inc"
#include "dt1_tab.inc"

static void gen_ym2151(void) {
    const int FREQ_SH = 16;
    const int ENV_BITS = 10;
    const int ENV_LEN  = 1 << ENV_BITS;
    const double ENV_STEP = 128.0 / ENV_LEN;
    const int SIN_LEN = 1024;
    const int TL_RES_LEN = 256;
    const int clock = 3579545;
    const int sampfreq = clock / 64;   /* 55930 */

    out = fopen("Src/SoundChips/DetTablesYm2151.h", "w");
    header("DET_TABLES_YM2151_H", "Tables for MameYM2151.c. clock=3579545 sampfreq=55930.");

    /* tl_tab base values (13-bit, before per-octave shifts) */
    {
        int32_t base[256];
        for (int x = 0; x < TL_RES_LEN; x++) {
            double m = (1<<16) / pow(2, (x+1) * (ENV_STEP/4.0) / 8.0);
            m = floor(m);
            int n = (int)m;
            n >>= 4;
            n = (n & 1) ? (n>>1)+1 : n>>1;
            n <<= 2;
            base[x] = n;
        }
        emit_i32("detYm2151TlBase", base, 256);
    }
    /* sin_tab */
    {
        uint32_t st[1024];
        for (int i = 0; i < SIN_LEN; i++) {
            double m = sin(((i*2)+1) * OPL_PI / SIN_LEN);
            double o = (m > 0.0) ? 8*log(1.0/m)/log(2) : 8*log(-1.0/m)/log(2);
            o = o / (ENV_STEP/4);
            int n = (int)(2.0*o);
            n = (n & 1) ? (n>>1)+1 : n>>1;
            st[i] = n*2 + (m >= 0.0 ? 0 : 1);
        }
        emit_u32("detYm2151SinTab", st, 1024);
    }
    /* d1l_tab: verify (i!=15?i:i+16)*32 */
    {
        uint32_t t[16];
        for (int i = 0; i < 16; i++) {
            double m = (uint32_t)((i != 15 ? i : i+16) * (4.0/ENV_STEP));
            t[i] = (uint32_t)m;
            CHECK(t[i] == (uint32_t)((i != 15 ? i : i+16) * 32), "ym2151 d1l %d", i);
        }
        emit_u32("detYm2151D1lTab", t, 16);
    }
    /* freq octave-2 base: verify exact-integer form, then bake */
    {
        uint32_t f2[768];
        double scaler = ((double)clock / 64.0) / (double)sampfreq;
        double mult = 1 << (FREQ_SH - 10);
        for (int i = 0; i < 768; i++) {
            double phaseinc = phaseinc_rom[i] * scaler;
            uint32_t dv = ((uint32_t)(int)(phaseinc*mult)) & 0xffffffc0u;
            /* exact integer: trunc(rom * clock * 2^(FREQ_SH-10) / (64*sampfreq)) */
            uint64_t iv = ((uint64_t)phaseinc_rom[i] * (uint64_t)clock * (1ull<<(FREQ_SH-10)))
                          / (64ull * (uint64_t)sampfreq);
            CHECK(dv == (((uint32_t)iv) & 0xffffffc0u), "ym2151 freq %d: %u vs %u", i, dv, (uint32_t)iv & 0xffffffc0u);
            f2[i] = dv;
        }
        emit_u32("detYm2151Freq2", f2, 768);
    }
    /* dt1_freq positive half: verify + bake */
    {
        int32_t t[128];
        double mult = 1 << FREQ_SH;
        for (int j = 0; j < 4; j++) {
            for (int i = 0; i < 32; i++) {
                double Hz = ((double)dt1_tab[j*32+i] * ((double)clock/64.0)) / (double)(1<<20);
                double phaseinc = (Hz*SIN_LEN) / (double)sampfreq;
                int32_t dv = (int32_t)(phaseinc * mult);
                int64_t iv = ((int64_t)dt1_tab[j*32+i] * clock * SIN_LEN * (1ll<<FREQ_SH))
                             / (64ll * (1ll<<20) * sampfreq);
                CHECK(dv == (int32_t)iv, "ym2151 dt1 %d,%d: %d vs %d", j, i, dv, (int)iv);
                t[j*32+i] = dv;
            }
        }
        emit_i32("detYm2151Dt1Freq", t, 128);
    }
    /* noise_tab: verify + bake */
    {
        uint32_t t[32];
        double scaler = ((double)clock / 64.0) / (double)sampfreq;
        for (int i = 0; i < 32; i++) {
            int j = (i != 31 ? i : 30);
            j = 32 - j;
            j = (int)(65536.0 / (double)(j*32.0));
            uint32_t dv = (uint32_t)(j * 64 * scaler);
            uint64_t iv = ((uint64_t)j * 64ull * (uint64_t)clock) / (64ull * (uint64_t)sampfreq);
            CHECK(dv == (uint32_t)iv, "ym2151 noise %d: %u vs %u", i, dv, (unsigned)iv);
            t[i] = dv;
        }
        emit_u32("detYm2151NoiseTab", t, 32);
    }
    footer("DET_TABLES_YM2151_H");
    fclose(out);
}

