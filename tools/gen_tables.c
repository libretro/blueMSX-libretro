/*
 * gen_tables.c - one-shot build tool for blueMSX-libretro determinism work.
 *
 * Reproduces, bit-for-bit, every floating-point table computation that the
 * emulator used to perform at init time, and emits the results as integer
 * constant tables (DetTables*.h).  It also cross-checks the exact-integer
 * reimplementations used for the tables that remain computed at runtime
 * (fn_tab, dphase tables, YM2151 freq tables, ...) against the original
 * double-precision results and reports any mismatch.
 *
 * This program is NOT part of the shipped core.  The shipped core contains
 * no float/double at all; it consumes the headers this tool generates.
 */

#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <math.h>
#include <stdint.h>

static FILE* out;

static void emit_i32(const char* name, const int32_t* v, int n) {
    fprintf(out, "static const Int32 %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%d%s%s", v[i], i==n-1?"":",", (i%12)==11||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void emit_u32(const char* name, const uint32_t* v, int n) {
    fprintf(out, "static const UInt32 %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%uU%s%s", v[i], i==n-1?"":",", (i%12)==11||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void emit_i16(const char* name, const int16_t* v, int n) {
    fprintf(out, "static const Int16 %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%d%s%s", v[i], i==n-1?"":",", (i%16)==15||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void emit_uint(const char* name, const unsigned* v, int n) {
    fprintf(out, "static const unsigned %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%uU%s%s", v[i], i==n-1?"":",", (i%12)==11||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void emit_int(const char* name, const int* v, int n) {
    fprintf(out, "static const int %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%d%s%s", v[i], i==n-1?"":",", (i%12)==11||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void emit_i64(const char* name, const int64_t* v, int n) {
    fprintf(out, "static const Int64 %s[%d] = {\n", name, n);
    for (int i = 0; i < n; i++) fprintf(out, "%lldLL%s%s", (long long)v[i], i==n-1?"":",", (i%8)==7||i==n-1 ? "\n" : "");
    fprintf(out, "};\n\n");
}
static void header(const char* guard, const char* what) {
    fprintf(out,
        "/* AUTOGENERATED by tools/gen_tables.c - DO NOT EDIT.\n"
        " * %s\n"
        " * Frozen integer tables; values identical to the double-precision\n"
        " * computation historically performed at init (IEEE-754, x86-64 libm).\n"
        " */\n#ifndef %s\n#define %s\n\n#include \"MsxTypes.h\"\n\n", what, guard, guard);
}
static void footer(const char* guard) { fprintf(out, "#endif /* %s */\n", guard); }

static int g_fail = 0;
#define CHECK(cond, ...) do { if(!(cond)) { g_fail=1; printf("MISMATCH: " __VA_ARGS__); printf("\n"); } } while(0)

