        CHECK(lfo_am_inc == (unsigned)(((1ull<<LFO_SH) * num) / (den * 64ull)), "262 am_inc");
        CHECK(lfo_pm_inc == (unsigned)(((1ull<<LFO_SH) * num) / (den * 1024ull)), "262 pm_inc");
